#ifndef AGENDA_H
#define AGENDA_H

#include "edge.h"

////////////////////////////////////////////////////////////////////////////////
// Class: Agenda
// Provides the interface for an Agenda.
////////////////////////////////////////////////////////////////////////////////
class Agenda {
public:
////////////////////////////////////////////////////////////////////////////////
// Function: add
// If the Edge is not in the Agenda, it is added to the Agenda.
////////////////////////////////////////////////////////////////////////////////
  void
  add(const Edge &);

////////////////////////////////////////////////////////////////////////////////
// Function: getNext
// Get the next Edge off the Agenda.  The Edge is removed from the
// Agenda.
////////////////////////////////////////////////////////////////////////////////
  Edge
  getNext();

////////////////////////////////////////////////////////////////////////////////
// Function: isIn
// Return true if the Edge is in the Agenda, false otherwise.
////////////////////////////////////////////////////////////////////////////////
  bool
  isIn(const Edge &) const;

////////////////////////////////////////////////////////////////////////////////
// Function: empty
// Return true if the Agenda is empty, false otherwise.
////////////////////////////////////////////////////////////////////////////////
  bool
  empty() const;

protected:
private:
////////////////////////////////////////////////////////////////////////////////
// Variable: _edges
// Store the Edges that are in the Agenda.
////////////////////////////////////////////////////////////////////////////////
   EdgeList _edges;

}; // end of class: Agenda

#endif
// end of file: agenda.h
